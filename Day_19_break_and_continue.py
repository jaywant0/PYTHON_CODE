

print("Break statement uses to break the loop")
for i in range(12):
    if(i == 10):
        break
    print("5 X ", i+1 , "=", 5 *(i+1))
print("loop is closed now")




print("Continue statement uses skip the iteration")
for i in range(12):
    if(i == 10):
        continue
    print("5 X ", i , "=", 5 *i)
    

print("loop is closed now")

